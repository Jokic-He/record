package com.sunwin.talent.entity;

import java.io.Serializable;
import java.util.Date;

public class talent implements Serializable{
	
	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	
	//简历id
	private String id;
	
	//openid
	private String openid;
	
	private String name;
	
	//性别 0女 1男
	private int sex;
	
	private Date birth;
	
	//地址
	private String address;
	
	//毕业院校
	private String school;
	
	//学历
	private String xueli;
	
	//专业
	private String major;
	
	//手机
	private String tel;
	
	private String email;
	
	//工作履历
	private String resume;
	
	//个人介绍
	private String introduce;
	
	

	public talent() {
		super();
	}

	public talent(String id, String openid, String name, int sex, Date birth, String address, String school, String xueli,
			String major, String tel, String email, String resume, String introduce) {
		super();
		this.id = id;
		this.openid = openid;
		this.name = name;
		this.sex = sex;
		this.birth = birth;
		this.address = address;
		this.school = school;
		this.xueli = xueli;
		this.major = major;
		this.tel = tel;
		this.email = email;
		this.resume = resume;
		this.introduce = introduce;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getOpenid() {
		return openid;
	}

	public void setOpenid(String openid) {
		this.openid = openid;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getSex() {
		return sex;
	}

	public void setSex(int sex) {
		this.sex = sex;
	}

	public Date getBirth() {
		return birth;
	}

	public void setBirth(Date birth) {
		this.birth = birth;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public String getSchool() {
		return school;
	}

	public void setSchool(String school) {
		this.school = school;
	}

	public String getXueli() {
		return xueli;
	}

	public void setXueli(String xueli) {
		this.xueli = xueli;
	}

	public String getMajor() {
		return major;
	}

	public void setMajor(String major) {
		this.major = major;
	}

	public String getTel() {
		return tel;
	}

	public void setTel(String tel) {
		this.tel = tel;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getResume() {
		return resume;
	}

	public void setResume(String resume) {
		this.resume = resume;
	}

	public String getIntroduce() {
		return introduce;
	}

	public void setIntroduce(String introduce) {
		this.introduce = introduce;
	}
	
	

}
