package com.sunwin.record.entity;

import java.io.Serializable;

public class tongdao implements Serializable{
	
	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	
	private String tongdao;
	
	private String fenjihao;
	
	

	public tongdao() {
		super();
	}

	public tongdao(String tongdao, String fenjihao) {
		super();
		this.tongdao = tongdao;
		this.fenjihao = fenjihao;
	}

	public String getTongdao() {
		return tongdao;
	}

	public void setTongdao(String tongdao) {
		this.tongdao = tongdao;
	}

	public String getFenjihao() {
		return fenjihao;
	}

	public void setFenjihao(String fenjihao) {
		this.fenjihao = fenjihao;
	}
	
	

}
